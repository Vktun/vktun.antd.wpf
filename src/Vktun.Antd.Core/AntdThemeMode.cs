namespace Vktun.Antd;

/// <summary>
/// Represents the supported theme modes.
/// </summary>
public enum AntdThemeMode
{
    /// <summary>
    /// Uses the light token set.
    /// </summary>
    Light,

    /// <summary>
    /// Uses the dark token set.
    /// </summary>
    Dark,

    /// <summary>
    /// Uses the light token set with compact spacing.
    /// </summary>
    Compact,
}
